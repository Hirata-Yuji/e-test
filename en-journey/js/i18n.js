// ── Current Language ─────────────────────────────
let currentLang = localStorage.getItem('ej_lang') || 'ja';

function setLang(lang) {
  if (!i18n[lang]) return;
  currentLang = lang;
  localStorage.setItem('ej_lang', lang);
  applyTranslations();
  updateLangButtons();
}

function t(key) {
  return (i18n[currentLang] && i18n[currentLang][key]) || (i18n['ja'][key]) || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      el.innerHTML = val;
    }
  });
  // lang attribute on html
  const langMap = { ja:'ja', en:'en', zh:'zh-CN', th:'th', ko:'ko' };
  document.documentElement.lang = langMap[currentLang] || 'ja';
}

function updateLangButtons() {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  applyTranslations();
  updateLangButtons();
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => setLang(btn.dataset.lang));
  });
});
