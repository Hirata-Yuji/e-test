def analyze(year, month, day):
    """九星気学の総合鑑定を行う"""
    honmeisei = calc_honmeisei(year, month, day)
    getsumeisei = calc_getsumeisei(year, month, day)
    nichimeisei = calc_nichimeisei(year, month, day)

    honmei_detail = KYUSEI_PERSONALITY.get(honmeisei, {})
    getsu_detail = KYUSEI_PERSONALITY.get(getsumeisei, {})
    nichi_detail = KYUSEI_PERSONALITY.get(nichimeisei, {})

    # 相性の良い九星
    gogyo = KYUSEI_GOGYO[honmeisei]
    soushou_map = {"木": "火", "火": "土", "土": "金", "金": "水", "水": "木"}
    aishou_gogyo = soushou_map.get(gogyo, "")
    good_match = [k for k, v in KYUSEI_GOGYO.items() if v == aishou_gogyo]

    return {
        "本命星": honmeisei,
        "月命星": getsumeisei,
        "日命星": nichimeisei,
        "本命星の詳細": honmei_detail,
        "月命星の詳細": {
            "基本性格": getsu_detail.get("基本性格", ""),
            "説明": f"月命星は内面の性格や私生活での傾向を表します。{getsumeisei}の影響で、" +
                    getsu_detail.get("基本性格", "")[:50] + "...という内面を持ちます。"
        },
        "日命星の詳細": {
            "基本性格": nichi_detail.get("基本性格", ""),
            "説明": f"日命星は隠れた本質や潜在的な性格を表します。{nichimeisei}の影響で、" +
                    nichi_detail.get("基本性格", "")[:50] + "...という本質を持ちます。"
        },
        "相性の良い九星": good_match,
        "五行": gogyo,
    }
